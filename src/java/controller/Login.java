    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("login.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            String username = (String) request.getParameter("username");
            String password = (String) request.getParameter("password");

            User user = new User();
            user.setUserName(username);
            user.setPassWord(password);
            UserDAO udao = new UserDAO();

            User auUser = udao.checkLogIn(user);

            System.out.println(auUser);
            System.out.println(auUser);
            if (auUser != null) {
                if (auUser.getIdDonhang() == null || auUser.getIdDonhang() == 0) {
                    auUser.setIdDonhang(udao.insertDonHang(auUser.getId()));
                }
                HttpSession session = request.getSession();
                session.setAttribute("user", auUser);
                if (auUser.getIsAdmin() == 1) {
                    response.sendRedirect("/demo10/admin");
                } else {
                    response.sendRedirect("/demo10/home");
                }
            } else {
                PrintWriter out = response.getWriter();
                out.print("sai mat khau");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

